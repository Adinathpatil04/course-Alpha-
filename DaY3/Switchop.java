public class Switchop {
    public static void main(String[] args){
        int number=3;

        switch(number){
            case 1: System.out.println("Sampoo");
               break;
            case 2: System.out.println("SSmosa");
               break;
            case 3: System.out.println("Milk");
               break;
            default: System.out.println("None of the above");

        }
    }
}
